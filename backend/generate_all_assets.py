def _existing_asset_file(module_dir: Path, key: str, fmt: str, topic: str = "", lm_code: str = "") -> Path | None:
    """Return the existing on-disk file for `key`, or None. Used by skip-existing.

    Tries the new naming convention `<TOPIC>-<LM>-<key>.<ext>` first, then falls
    back to the legacy bare `<key>.<ext>` so older modules keep working.
    """
    if fmt == "json":
        exts = [".json"]
    elif fmt == "md":
        exts = [".md"]
    elif fmt == "dot":
        exts = [".svg", ".dot"]
    else:
        exts = [".pdf"]
    if topic and lm_code:
        return find_asset_path(module_dir, topic, lm_code, key, exts)
    # Legacy fallback (callers that don't pass topic/lm_code)
    for ext in exts:
        p = module_dir / f"{key}{ext}"
        if p.exists():
            return p
    return None


def generate_all(topic: str, lm_code: str, lm_title: str, skip_existing: bool = False):
    """Generate all 16 assets for a given LM using the Claude API.

    When `skip_existing` is True, assets whose output file is already present
    on disk are left untouched — the run only fills the gaps. Stdout still
    prints a "Generating ... OK (skipped)" line so progress trackers stay
    aligned with the canonical 17-step sequence.
    """
    module_dir = GENERATED_ROOT / topic / lm_code
    module_dir.mkdir(parents=True, exist_ok=True)

    assets = []
    start = time.time()

    for key, (title, prompt_builder, fmt) in GENERATORS.items():
        if skip_existing:
            existing = _existing_asset_file(module_dir, key, fmt, topic, lm_code)
            if existing is not None:
                size_kb = round(existing.stat().st_size / 1024, 1)
                assets.append({"title": title, "filename": existing.name, "size_kb": size_kb, "status": "success"})
                print(f"  Generating {key} ({title})... OK (skipped, {size_kb} KB)", flush=True)
                continue

        print(f"  Generating {key} ({title})...", end=" ", flush=True)

        prompt = prompt_builder(topic, lm_code, lm_title)
        content = _safe_generate(prompt, title)

        if fmt == "json":
            # Clean up potential markdown code fences from the response
            cleaned = content.strip()
            if cleaned.startswith("```"):
                # Remove opening fence (```json or ```)
                first_newline = cleaned.index("\n")
                cleaned = cleaned[first_newline + 1:]
            if cleaned.endswith("```"):
                cleaned = cleaned[:-3].rstrip()
            # Validate JSON
            try:
                json.loads(cleaned)
                content = cleaned
            except json.JSONDecodeError:
                print("(Invalid JSON, using raw content)", end=" ")

            filename = asset_filename(topic, lm_code, key, ".json")
            filepath = module_dir / filename
            filepath.write_text(content, encoding="utf-8")
        elif fmt == "md":
            filename = asset_filename(topic, lm_code, key, ".md")
            filepath = module_dir / filename
            filepath.write_text(content, encoding="utf-8")
        elif fmt == "dot":
            dot_src = _clean_dot(content)
            dot_filename = asset_filename(topic, lm_code, key, ".dot")
            dot_path = module_dir / dot_filename
            dot_path.write_text(dot_src, encoding="utf-8")
            svg_filename = asset_filename(topic, lm_code, key, ".svg")
            svg_path = module_dir / svg_filename
            if _render_dot_to_svg(dot_src, svg_path) and svg_path.exists():
                filename = svg_filename
                filepath = svg_path
            else:
                filename = dot_filename
                filepath = dot_path
        else:
            filename = asset_filename(topic, lm_code, key, ".pdf")
            filepath = module_dir / filename
            _markdown_to_pdf(content, str(filepath), title, f"{topic}-{lm_code}")

        size_kb = round(filepath.stat().st_size / 1024, 1)
        assets.append({"title": title, "filename": filename, "size_kb": size_kb, "status": "success"})
        print(f"OK ({size_kb} KB)")

    elapsed = round(time.time() - start, 1)

    # Write manifest
    manifest = {
        "module": {"topic": topic, "code": f"{topic}-{lm_code}", "title": lm_title},
        "generated_at": time.strftime("%Y-%m-%d %H:%M:%S"),
        "total_time_sec": elapsed,
        "assets": assets,
    }
    manifest_path = module_dir / "manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2, ensure_ascii=False), encoding="utf-8")

    print(f"\n  Done! {len(assets)} assets in {elapsed}s -> {module_dir}")
    return manifest


