# ── Claude API helper ───────────────────────────────────────

MODEL = "claude-sonnet-4-20250514"
MAX_TOKENS = 4096


def _call_claude(prompt: str) -> str:
    """Call the Anthropic API and return the text response."""
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        raise RuntimeError(
            "ANTHROPIC_API_KEY is not set. "
            "Export it before running: export ANTHROPIC_API_KEY=sk-ant-..."
        )
    client = anthropic.Anthropic(api_key=api_key)
    message = client.messages.create(
        model=MODEL,
        max_tokens=MAX_TOKENS,
        messages=[{"role": "user", "content": prompt}],
    )
    return message.content[0].text


def _clean_dot(raw: str) -> str:
    """Strip Markdown fences from a DOT response."""
    s = raw.strip()
    if s.startswith("```"):
        nl = s.index("\n")
        s = s[nl + 1:]
    if s.endswith("```"):
        s = s[:-3].rstrip()
    return s


def _render_dot_to_svg(dot_src: str, out_path: Path) -> bool:
    """Render DOT source to SVG via the `dot` binary. Returns True on success."""
    import shutil, subprocess
    if shutil.which("dot") is None:
        print("(graphviz `dot` not installed, SVG skipped)", end=" ")
        return False
    try:
        result = subprocess.run(
            ["dot", "-Tsvg", "-o", str(out_path)],
            input=dot_src.encode("utf-8"),
            capture_output=True,
            timeout=30,
        )
        if result.returncode != 0:
            print(f"(dot failed: {result.stderr.decode(errors='replace')[:120]})", end=" ")
            return False
        return True
    except Exception as exc:
        print(f"(dot error: {exc})", end=" ")
        return False


def _safe_generate(prompt: str, asset_label: str) -> str:
    """Call Claude with error handling; return placeholder on failure."""
    try:
        return _call_claude(prompt)
    except Exception as exc:
        print(f"\n    [API ERROR] {asset_label}: {exc}")
        return (
            f"# {asset_label}\n\n"
            f"**Error during automatic generation.**\n\n"
            f"The content could not be generated by the Claude API.\n"
            f"Error: {exc}\n\n"
            f"Please rerun the generation or write the content manually."
        )


